	CxList assigned = Find_Assign_Lefts();
	assigned.Add(methodDecls);
	
	CxList sap = Find_SAP_Library();
	CxList newSAP = Find_ObjectCreations() * sap;
	CxList unknownReferences = Find_UnknownReference();
	CxList associativeArrays = Find_AssociativeArrayExpr();
	CxList methodInvokes = Find_Methods();	
	CxList returnStmt = Find_ReturnStmt();
	
	foreach (string str in list)
	{
		string[] namespaces = str.Split(new char[] {'.', '#'});
		int size = namespaces.Length;
		if (size < 2)
		{
			continue;
		}
		string firstmember = namespaces[0];
		if (!firstmember.Equals("$") && !firstmember.Equals("jQuery") && !firstmember.Equals("sap") )
		{
			continue;
		}
		string lastmember = namespaces[size - 1];
		if (lastmember.Contains("events:"))
		{
			// look for event handler
			lastmember = lastmember.Replace("events:", "");
			string name = str.Substring(0, str.IndexOf('#'));
			CxList newObj = newSAP.FindByType(name);
			CxList parameters = All.GetParameters(newObj, 1);
			parameters -= Find_Parameters();
		
			//Get AssociativeArraysExpr parameters
			CxList associativeArraysParams = parameters * associativeArrays;
		
			//Get AssociativeArraysExpr that relate to Unknown References as parameters
			CxList associativeArraysWithProperty = associativeArrays.FindByAbstractValue(abstractValue =>
				abstractValue is ObjectAbstractValue &&
				((ObjectAbstractValue) abstractValue).GetField(lastmember) != null) - associativeArraysParams; 
			CxList associativeArraysRelatedWithParameters =
				associativeArraysWithProperty.GetAssignee(unknownReferences.FindAllReferences(parameters))
				.GetAssigner();
			associativeArraysParams.Add(associativeArraysRelatedWithParameters);
			associativeArraysWithProperty -= associativeArraysRelatedWithParameters;
		
			//Get AssociativeArraysExpr in method invokes as parameter (explicit in return)
			CxList methodInvokesDef = methodDecls.FindDefinition(methodInvokes * parameters);
			CxList assocArraysInReturns = associativeArraysWithProperty.FindByFathers(returnStmt);
			assocArraysInReturns = assocArraysInReturns.GetByMethod(methodInvokesDef);
			associativeArraysParams.Add(assocArraysInReturns);
			associativeArraysWithProperty -= assocArraysInReturns;
		
			//Get AssociativeArraysExpr in method invokes as parameter (UnknownReference in return)
			CxList unkRefInReturns = unknownReferences.FindByFathers(returnStmt);
			unkRefInReturns = unkRefInReturns.GetByMethod(methodInvokesDef);
			CxList assocArrRelated =
				associativeArraysWithProperty.GetAssignee(unknownReferences.FindAllReferences(unkRefInReturns))
				.GetAssigner();
			associativeArraysParams.Add(assocArrRelated);
		
			//Get relevant members
			result.Add(assigned.GetByAncs(associativeArraysParams).FindByShortName(lastmember));
		
			continue;
		}
		else if (str.Contains("#"))
		{
			// look for instance method
			string name = str.Substring(0, str.IndexOf('#'));
			CxList newObj = newSAP.FindByType(name);
			CxList assignedObj = newObj.GetAssignee();
			assignedObj.Add(All.FindAllReferences(assignedObj));
			result.Add(assignedObj.GetMembersOfTarget().FindByShortName(lastmember));
		}
		else
		{
			result.Add(newSAP.FindByType(str + "*"));
		}
		// look for class/library/namespace/static methods
		string currentMember = namespaces[1];
		CxList current = sap.FindByShortName(currentMember); 
		for (int i = 2; i < size; i++)
		{
			currentMember = namespaces[i];
			current = current.GetMembersOfTarget().FindByShortName(currentMember);
		}
		CxList remainingTargets = current.GetMembersOfTarget();
		if(remainingTargets.Count == 0 || (remainingTargets * methodInvokes).Count > 0) {
			result.Add(current);
		}
	}

	result -= result.GetTargetOfMembers();
